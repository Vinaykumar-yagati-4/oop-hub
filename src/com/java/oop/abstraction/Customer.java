package com.java.oop.abstraction;

public class Customer {
    int id;
    String name;
    String email;
    long mobileNO;
    String aadharNo;

    public Customer(int id, String name, String email, long mobileNO, String aadharNo) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.mobileNO = mobileNO;
        this.aadharNo = aadharNo;
    }
}
